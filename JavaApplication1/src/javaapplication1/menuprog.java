    private void datos_tabla() {
        
        int cont = 0;     
        try { //para las consultas sql siempre vamos a ocupar un try catch por su ocurre un error
            Statement st_cont = conexionbd.conn.createStatement(); //el statement nos ayuda a procesar una sentencia sql 
            ResultSet rs_cont = st_cont.executeQuery("SELECT COUNT(*) AS count FROM Vehiculos"); 
            Statement st = conexionbd.conn.createStatement(); //ahora vamos a  hacer lo mismo solo que esta vez no obtendremos el numero de filas en la tabla
            rs = st.executeQuery("SELECT * FROM Vehiculos"); //ahora obtendremos los datos de la tabla para mostrarlos en el jtable
            while (rs_cont.next()){
                cont = rs_cont.getInt("count");//sirve para asignar la cantidad de vehiculos a la variable count
            }
            //el contador nos ayudara para movernos en las filas de la matriz mientras que los numeros fijos (0,1,2,3,4,5) nos moveran por las 6 columnas que seran el id, nombre, etc
            M_datos = new String[cont][6]; //definimos el tama単o de la matriz 
            int c = 0;
            while (c < cont){
                while (rs.next()) { //el while nos ayudara a recorrer los datos obtenidos en la consulta anterior y asignarlos a la matriz  
                    M_datos[c][0] = rs.getString("marca");    //agregamos los datos a la tabla
                    M_datos[c][1] = rs.getString("modelo");
                    M_datos[c][2] = rs.getString("matricula");
                    M_datos[c][3] = rs.getString("disponibilidad");
                    M_datos[c][4] = rs.getString("oficina");
                    M_datos[c][5] = rs.getString("color");
                    c++;//avanzamos una posicion del contador para que pase a la siguiente fila
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(menuprog.class.getName()).log(Level.SEVERE, null, ex); //si llegara a ocurrir un error ya se  una mala consulta o mala conexion aqui nos lo mostraria
        }

        dtm_datos = new DefaultTableModel(M_datos, Titulos) { //ahora agregaremos la matriz y los titulos al modelo de tabla
            @Override
            public boolean isCellEditable(int row, int column) {//este metodo es muy util si no quieren que editen su tabla, 
                return false;  //si quieren modificar los campos al dar clic entonces borren este metodo
            }
        };
        jtable_datos.setModel(dtm_datos); //ahora el modelo que ya tiene tanto los datos como los titulos lo agregamos a la tabla
        trs = new TableRowSorter<>(dtm_datos); //iniciamos el table row sorter para ordenar los datos (esto es si gustan)
        jtable_datos.setRowSorter(trs); //y lo agregamos al jtable
    }
